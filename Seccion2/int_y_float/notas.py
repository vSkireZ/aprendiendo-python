#Tipos
print(42) # int (entero)
print(3.13) # float (decimal)

#Operaciones básicas
print(10 + 5) # Suma: 15
print(10 - 5) # Resta : 5
print(10 * 5) # Multiplicación: 50
print(10 / 5) # División : 2.0 (siempre float)

#Operaciones especiales
print(17 // 5) #División entera: 3
print(17 % 5) #Módulo (resto): 2
print(2 ** 3) #Potencia: 8

#Conversiones
print(int(3.7)) # 3 (trunca)
print(float(5)) # 5.0
print(round(3.7)) # 4 (redondea)
print(round(3.14159 , 2)) # 3.14 






