
#Print - Palabra clave
#Color verde - Textos o Strings

print("Hola Mundo")
#Puede recibir varios valores separados por coma y pone un espacio entre ellos automaticamente
print("Hola", "Mundo")

#Alternativas
#Palabras entre comillas
#Python acepta " y ' indistintamente, pero lo que abres debes cerrarlo con lo mismo.


print("Hola Mundo te saludo diciendo 'Hola'")
print('Hola Mundo te saludo diciendo "Hola"')

#Utilizando la barra invertida
#La barra invertida \" se llama carácter de escape le dice a python que el siguiente caracter es especial, no literal.
print("Hola Mundo te saludo diciendo \"Hola\"")
print('Hola Mundo te saludo diciendo \'Hola\'')

