#Strings: Cadenas de caracteres

#Concatenacion
print("Hola" + "Miguel") #HolaMiguel

#Saltos de linea
print("Esta es la primera linea\nEsta es la segunda linea")

#Tabulacion
print("Nombre\tApellido\tEdad")

#Comillas simples
print('Hola Miguel')

#Triple comilla: Permite escribir texto en varias lineas sin necesidad de usar el salto de linea \n
print("""Linea 1
Linea 2
Linea 3""")

#f strings: Permite insertar variables dentro de una cadena de texto
nombre = "Miguel"
edad = 30
print(f"Hola {nombre} tienes {edad} anios")


